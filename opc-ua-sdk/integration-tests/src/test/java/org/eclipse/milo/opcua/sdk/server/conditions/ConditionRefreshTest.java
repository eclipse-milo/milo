    // Unknown subscription id.
    CallMethodResult unknown =
        call(
            NodeIds.ConditionType,
            NodeIds.ConditionType_ConditionRefresh,
            new Variant(uint(Integer.MAX_VALUE)));
    assertEquals(StatusCode.of(StatusCodes.Bad_SubscriptionIdInvalid), unknown.getStatusCode());

