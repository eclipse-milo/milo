  @Test
  void markersBypassFiltersAndRetainedStateReplaysWithOriginalIdentity() throws Exception {
    var subscription = new OpcUaSubscription(client);
    subscription.create();

    try {
      List<Variant[]> matching = monitorEvents(subscription, TEST_SEVERITY);
      List<Variant[]> excluded = monitorEvents(subscription, OTHER_SEVERITY);

      condition.setAcked(false);
      EventTestSupport.awaitEvent(
          matching, "live unacknowledged event", e -> "Unacknowledged".equals(messageOf(e)));
      matching.clear();

      ByteString lastEventId = condition.currentBranch().getLastEventId();
      DateTime lastEventTime = condition.currentBranch().getLastEventTime();

      CallMethodResult result = conditionRefresh(subscription);
      assertGood(result);

      // The item whose where clause matches the condition's events: full bracket plus replay.
      Variant[] start =
          EventTestSupport.awaitEvent(
              matching,
              "RefreshStart marker",
              e -> NodeIds.RefreshStartEventType.equals(eventTypeOf(e)));
      Variant[] replay =
          EventTestSupport.awaitEvent(
              matching, "replayed condition event", e -> lastEventId.equals(eventIdOf(e)));
      Variant[] end =
          EventTestSupport.awaitEvent(
              matching,
              "RefreshEnd marker",
              e -> NodeIds.RefreshEndEventType.equals(eventTypeOf(e)));

      // The replayed state carries the original identity and current retained state.
      assertEquals(lastEventTime, replay[4].value(), "replay must carry the original Time");
      assertEquals("Unacknowledged", messageOf(replay));
      assertTrue(retainOf(replay));
      assertEquals(conditionId, conditionIdOf(replay), "ConditionId must resolve on replay");

      // The bracket is ordered around the replay.
      assertTrue(matching.indexOf(start) < matching.indexOf(replay), "RefreshStart before replay");
      assertTrue(matching.indexOf(replay) < matching.indexOf(end), "replay before RefreshEnd");

      // The item whose where clause excludes everything: markers arrive anyway (§4.5 bypass),
      // replayed events respect the filter.
      Variant[] excludedStart =
          EventTestSupport.awaitEvent(
              excluded,
              "RefreshStart marker on filter-excluded item",
              e -> NodeIds.RefreshStartEventType.equals(eventTypeOf(e)));
      EventTestSupport.awaitEvent(
          excluded,
          "RefreshEnd marker on filter-excluded item",
          e -> NodeIds.RefreshEndEventType.equals(eventTypeOf(e)));
      EventTestSupport.assertNoEvent(
          excluded,
          "replayed event on filter-excluded item",
          e -> lastEventId.equals(eventIdOf(e)));

      // One shared EventId per bracket marker across the subscription's items.
      assertEquals(eventIdOf(start), eventIdOf(excludedStart));
    } finally {
      subscription.delete();
    }
  }

