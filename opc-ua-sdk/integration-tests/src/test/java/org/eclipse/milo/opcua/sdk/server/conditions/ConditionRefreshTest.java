  @Test
  void concurrentRefreshReturnsRefreshInProgressWhileOthersAreAccepted() throws Exception {
    var subscription1 = new OpcUaSubscription(client);
    subscription1.create();
    var subscription2 = new OpcUaSubscription(client);
    subscription2.create();

    var entered = new CountDownLatch(1);
    var release = new CountDownLatch(1);

    // Fired under the Condition's lock: blocking here keeps the lock held, which stalls a refresh
    // at snapshot creation — after its per-subscription guard is engaged, but without touching any
    // subscription or item monitor the publish pipeline uses.
    EventListener blockingListener =
        eventNode -> {
          if (eventNode == condition.getNode()) {
            entered.countDown();
            try {
              release.await(10, TimeUnit.SECONDS);
            } catch (InterruptedException e) {
              Thread.currentThread().interrupt();
            }
          }
        };

    try {
      monitorEvents(subscription1, TEST_SEVERITY);
      monitorEvents(subscription2, TEST_SEVERITY);

      condition.setAcked(false);

      UInteger subscription1Id = subscription1.getSubscriptionId().orElseThrow();
      UInteger subscription2Id = subscription2.getSubscriptionId().orElseThrow();
      Session serverSession = server.getSubscriptions().get(subscription1Id).getSession();

      server.getEventNotifier().register(blockingListener);

      var toggler = new Thread(() -> condition.setAcked(true));
      toggler.start();

      assertTrue(entered.await(5, TimeUnit.SECONDS), "the Condition lock was not engaged in time");

      var firstRefresh = new CompletableFuture<Void>();
      var refreshThread =
          new Thread(
              () -> {
                try {
                  server.getConditionManager().conditionRefresh(serverSession, subscription1Id);
                  firstRefresh.complete(null);
                } catch (Throwable t) {
                  firstRefresh.completeExceptionally(t);
                }
              });
      refreshThread.start();

      // Wait until the refresh is parked at snapshot creation: its guard is engaged from that
      // point until it completes, which the held Condition lock prevents.
      long deadline = System.currentTimeMillis() + 5_000;
      while (!isBlockedCreatingSnapshots(refreshThread)) {
        if (System.currentTimeMillis() > deadline) {
          fail("refresh did not block at snapshot creation");
        }
        //noinspection BusyWait
        Thread.sleep(10);
      }

      // A concurrent refresh of the same subscription is rejected...
      UaException e =
          assertThrows(
              UaException.class,
              () -> server.getConditionManager().conditionRefresh(serverSession, subscription1Id));
      assertEquals(StatusCode.of(StatusCodes.Bad_RefreshInProgress), e.getStatusCode());

      // ...while a refresh of a different subscription is accepted (it serializes on the shared
      // Condition lock but is not rejected by subscription 1's guard).
      var secondRefresh = new CompletableFuture<Void>();
      var refresh2Thread =
          new Thread(
              () -> {
                try {
                  server.getConditionManager().conditionRefresh(serverSession, subscription2Id);
                  secondRefresh.complete(null);
                } catch (Throwable t) {
                  secondRefresh.completeExceptionally(t);
                }
              });
      refresh2Thread.start();

      release.countDown();

      firstRefresh.get(5, TimeUnit.SECONDS);
      secondRefresh.get(5, TimeUnit.SECONDS);
      toggler.join(5_000);
    } finally {
      release.countDown();
      server.getEventNotifier().unregister(blockingListener);
      subscription1.delete();
      subscription2.delete();
    }
  }

  private static boolean isBlockedCreatingSnapshots(Thread thread) {
    Thread.State state = thread.getState();

    if (state != Thread.State.WAITING
        && state != Thread.State.TIMED_WAITING
        && state != Thread.State.BLOCKED) {
      return false;
    }

    return Stream.of(thread.getStackTrace())
        .anyMatch(frame -> "createRefreshSnapshots".equals(frame.getMethodName()));
  }

  @Test
  void refreshBoundaryPrecedesSnapshotCaptureAndLiveRetraction() throws Exception {
    var subscription = new OpcUaSubscription(client);
    subscription.create();

    try {
      List<Variant[]> events = monitorEvents(subscription, TEST_SEVERITY);

      condition.setAcked(false);
      ByteString replayEventId = condition.currentBranch().getLastEventId();
      pausingCondition.setAcked(false);
      ByteString preRefreshEventId = pausingCondition.currentBranch().getLastEventId();
      EventTestSupport.awaitEvent(
          events, "live retained event to replay", e -> replayEventId.equals(eventIdOf(e)));
      EventTestSupport.awaitEvent(
          events, "live event before refresh", e -> preRefreshEventId.equals(eventIdOf(e)));
      events.clear();

      pausingCondition.enableSnapshotPause();

      UInteger subscriptionId = subscription.getSubscriptionId().orElseThrow();
      Session serverSession = server.getSubscriptions().get(subscriptionId).getSession();
      var refreshResult = new CompletableFuture<Void>();
      var refreshThread =
          new Thread(
              () -> {
                try {
                  server.getConditionManager().conditionRefresh(serverSession, subscriptionId);
                  refreshResult.complete(null);
                } catch (Throwable t) {
                  refreshResult.completeExceptionally(t);
                }
              });
      refreshThread.start();

      assertTrue(
          pausingCondition.awaitSnapshotCaptured(5, TimeUnit.SECONDS),
          "refresh did not reach snapshot capture");

      // Snapshot capture has been reached but not performed for the pausing Condition. This live
      // transition has a distinct EventId and must be queued after RefreshStart but before
      // RefreshEnd. With the old snapshots-before-start ordering it deterministically lands before
      // RefreshStart; after release the now-Retain=false Condition produces no replay snapshot.
      pausingCondition.setAcked(true);
      ByteString liveRetractionEventId = pausingCondition.currentBranch().getLastEventId();
      pausingCondition.releaseSnapshot();

      refreshResult.get(5, TimeUnit.SECONDS);
      refreshThread.join(5_000);

      EventTestSupport.awaitEvent(
          events, "RefreshStart marker", e -> NodeIds.RefreshStartEventType.equals(eventTypeOf(e)));
      EventTestSupport.awaitEvent(
          events, "RefreshEnd marker", e -> NodeIds.RefreshEndEventType.equals(eventTypeOf(e)));

      Variant[] replay =
          EventTestSupport.awaitEvent(
              events,
              "separate retained-condition replay",
              e -> replayEventId.equals(eventIdOf(e)));
      Variant[] liveRetraction =
          EventTestSupport.awaitEvent(
              events,
              "live Retain=false transition",
              e -> liveRetractionEventId.equals(eventIdOf(e)));

      assertEquals(1, countEventId(events, replayEventId), "snapshot identity must replay once");
      assertEquals(
          1,
          countEventId(events, liveRetractionEventId),
          "the distinct live identity must be delivered exactly once");
      assertEquals(
          0,
          countEventId(events, preRefreshEventId),
          "the retracted pausing Condition must not replay its pre-refresh state");
      assertTrue(retainOf(replay), "the separate retained Condition must replay Retain=true");
      assertTrue(!retainOf(liveRetraction), "live transition must retract Retain");

      int startIndex = indexOfEventType(events, NodeIds.RefreshStartEventType);
      int endIndex = indexOfEventType(events, NodeIds.RefreshEndEventType);
      int liveIndex = indexOfEventId(events, liveRetractionEventId);
      int replayIndex = indexOfEventId(events, replayEventId);
      assertTrue(
          startIndex >= 0 && liveIndex > startIndex && endIndex > liveIndex,
          "the distinct live Retain=false event must occur inside the marker bracket");
      assertTrue(
          replayIndex > startIndex && endIndex > replayIndex,
          "the separately identified replay must occur inside the marker bracket");
    } finally {
      pausingCondition.releaseSnapshot();
      subscription.delete();
    }
  }

